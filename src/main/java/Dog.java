













public class Dog {
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
    // Attributes to store the name and age of the dog
    private String name;
    private Integer age;

    // Constructor to initialize the Dog object with name and age
    public Dog(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    // Getter method for the dog's name
    public String getName() {
        return name;
    }

    // Setter method for the dog's name
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for the dog's age
    public Integer getAge() {
        return age;
    }

    // Setter method for the dog's age
    public void setAge(Integer age) {
        this.age = age;
    }
}
